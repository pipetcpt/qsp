def set_ko(run):
    run.font.name = "Malgun Gothic"
    run.element.rPr.rFonts.set(qn("w:eastAsia"), "맑은 고딕")

doc = Document()
st = doc.styles["Normal"]; st.font.name = "Malgun Gothic"; st.font.size = Pt(11)
st.element.rPr.rFonts.set(qn("w:eastAsia"), "맑은 고딕")
for s in doc.sections:
    s.top_margin = s.bottom_margin = Cm(2.2); s.left_margin = s.right_margin = Cm(2.5)

p = doc.add_paragraph(); r = p.add_run(TITLE); r.bold = True; r.font.size = Pt(13); set_ko(r)
doc.add_paragraph("")
for n, title, text in SLIDES:
    hp = doc.add_paragraph(); hp.paragraph_format.space_before = Pt(8); hp.paragraph_format.space_after = Pt(3)
    hr = hp.add_run(f"[슬라이드 {n}] {title}"); hr.bold = True; hr.font.size = Pt(11.5); hr.font.color.rgb = NAVY; set_ko(hr)
    for chunk in text.split("\n\n"):
        bp = doc.add_paragraph(chunk); bp.paragraph_format.line_spacing = 1.5; bp.paragraph_format.space_after = Pt(8)
        for rr in bp.runs: set_ko(rr)
doc.save(OUT)
