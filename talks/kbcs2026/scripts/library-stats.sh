#!/usr/bin/env bash
# library-stats.sh — recompute live QSP-library metrics from the repo root.
# Usage: bash talks/kbcs2026/scripts/library-stats.sh [repo_root]
set -euo pipefail
ROOT="${1:-$(git -C "$(dirname "$0")" rev-parse --show-toplevel 2>/dev/null || echo "$PWD")}"
cd "$ROOT"

mapfile -t MODEL_DIRS < <(
  for d in */; do
    d=${d%/}
    [ "$d" = "talks" ] && continue
    if ls "$d"/*_qsp*.dot >/dev/null 2>&1; then echo "$d"; fi
  done | sort
)
N_MODELS=${#MODEL_DIRS[@]}
N_REFS=$(grep -rhoE 'https://pubmed[^ )]*' --include='*references.md' . 2>/dev/null | wc -l | tr -d ' ')
N_CLUSTERS=$(grep -rho 'subgraph cluster' --include='*.dot' . 2>/dev/null | wc -l | tr -d ' ')
N_ODE=$(find . \( -name '*_mrgsolve_model.R' -o -name '*_model.R' \) ! -path './talks/*' | wc -l | tr -d ' ')
REFS_PER=$(awk "BEGIN{ if($N_MODELS>0) printf \"%.0f\", $N_REFS/$N_MODELS; else print 0 }")
CL_PER=$(awk "BEGIN{ if($N_MODELS>0) printf \"%.1f\", $N_CLUSTERS/$N_MODELS; else print 0 }")

cat <<YAML
# Auto-generated by scripts/library-stats.sh — bump on the talk day by re-running.
n_models: $N_MODELS
n_references: $N_REFS
refs_per_model: $REFS_PER
n_clusters: $N_CLUSTERS
clusters_per_model: $CL_PER
n_ode_systems: $N_ODE
YAML
