dxdt_Cp_imt  = ka_imt * Gut_imt / Vd_imt - (CL_imt / Vd_imt) * Cp_imt;
dxdt_Cic_imt = Kin_imt * Cp_imt - Kout_imt * Cic_imt;

// --- Dasatinib PK ---
// Continuous approximation (rapid Tmax, large Vd)
dxdt_Cp_das  = F_das * dose_rate_das / Vd_das - (CL_das / Vd_das) * Cp_das;
dxdt_Cic_das = Kic_das * Cp_das - Kout_das * Cic_das;

// --- Nilotinib PK ---
dxdt_Gut_nil = F_nil * dose_rate_nil - ka_nil * Gut_nil;
dxdt_Cp_nil  = ka_nil * Gut_nil / Vd_nil - (CL_nil / Vd_nil) * Cp_nil;
dxdt_Cic_nil = Kic_nil * Cp_nil - Kout_nil * Cic_nil;

// --- Asciminib PK ---
