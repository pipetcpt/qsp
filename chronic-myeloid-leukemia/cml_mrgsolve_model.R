// LPC2
double TKI_kill_x2 = E_total_sens * x2 * 0.8;
dxdt_x2 = lc2 * b1 * x1 - b2 * x2 - d2 * x2 - TKI_kill_x2;

// Leukemic WBC/blasts
double TKI_kill_x3 = E_total_sens * x3 * 1.0;  // most sensitive (post-mitotic)
double NK_kill_x3  = kNK_kill * NK_base * x3 / (x3 + 1e8);
double CTL_kill_x3 = kCTL_kill * CTL_base * x3 / (x3 + 1e8);

dxdt_x3 = lc3 * b2 * x2 - d3 * x3 - TKI_kill_x3 - NK_kill_x3 - CTL_kill_x3;

// ============================================================
// Sensitive Clone ODEs
// ============================================================
double TKI_kill_xs = E_total_sens * xs;
dxdt_xs  = as0 * xs - bs0 * xs - ds0 * xs - TKI_kill_xs - mu * as0 * xs;
dxdt_xsp = lc1 * bs0 * xs + asp * xsp - dsp * xsp - E_total_sens * xsp * 0.7;

// ============================================================
// Resistant Clone (T315I) ODEs
// ============================================================
double TKI_kill_xr  = E_total_res * xr;   // only asciminib 200mg hits this
double TKI_kill_xrp = E_total_res * xrp * 0.7;

dxdt_xr  = ar0 * xr - br0 * xr - dr0 * xr - TKI_kill_xr;
dxdt_xrp = lc1 * br0 * xr + arp * xrp - drp * xrp - TKI_kill_xrp;

$TABLE
// BCR-ABL IS% calculation
// IS% = CML cells / (CML + normal cells) × 100
// Approximate: total CML WBC / (total CML WBC + normal neutrophils)

double CML_total  = x3 + xrp + xsp;
double Norm_total = y3;
double BCRPCT     = 100.0 * CML_total / fmax(CML_total + Norm_total, 1.0);

// Log10 BCR-ABL IS
double LOG_IS = (BCRPCT > 0.0001) ? log10(BCRPCT) : -5.0;

// Molecular response milestones
double CHR_reached  = (BCRPCT < 10.0)      ? 1.0 : 0.0;
double MMR_reached  = (BCRPCT < 0.1)       ? 1.0 : 0.0;
double MR4_reached  = (BCRPCT < 0.01)      ? 1.0 : 0.0;
double MR45_reached = (BCRPCT < 0.0032)    ? 1.0 : 0.0;

// WBC count (10^9/L, assuming 5L blood volume)
double WBC_total_blood = (x3 + Norm_total) / 5e9;  // cells/L / (cells/count unit)

// PK summary
double Cp_active = Cp_imt + Cp_das + Cp_nil + Cp_asc;

// Resistant fraction
double Resist_frac = (xr + xrp) / fmax(CML_total, 1.0);

capture BCRPCT     = BCRPCT;
capture LOG_IS     = LOG_IS;
capture CHR        = CHR_reached;
capture MMR        = MMR_reached;
capture MR4        = MR4_reached;
capture MR45       = MR45_reached;
capture WBC        = WBC_total_blood;
capture LSC_total  = x0q + x0p + xs + xr;
capture LSC_quies  = x0q / fmax(x0q + x0p, 1.0);  // quiescent fraction
capture Resist_frac = Resist_frac;
capture E_imt_out  = E_imt;
capture E_das_out  = E_das;
capture E_nil_out  = E_nil;
capture E_asc_out  = E_asc;

$CAPTURE BCRPCT LOG_IS CHR MMR MR4 MR45 WBC LSC_total LSC_quies
         Resist_frac E_imt_out E_das_out E_nil_out E_asc_out
         Cp_imt Cic_imt Cp_das Cic_das Cp_nil Cic_nil Cp_asc
'

