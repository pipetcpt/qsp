// ============================================================
// Helper: MW conversions for dose (mg) → µmol
// Imatinib MW = 493.6 g/mol; Dasatinib = 488.0; Nilotinib = 529.5; Asciminib = 449.5
// Dose entered as mg/day; rate into gut = Dose/24 mg/h (continuous infusion approx)
