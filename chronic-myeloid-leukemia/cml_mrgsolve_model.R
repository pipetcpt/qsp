dxdt_Cp_asc  = ka_asc * Gut_asc / Vd_asc - (CL_asc / Vd_asc) * Cp_asc;

// ============================================================
// PD: Drug Effect Functions (Emax model, Hill equation)
// Effect = fraction of BCR-ABL-driven proliferation inhibited
// ============================================================

// Imatinib effect on sensitive (WT BCR-ABL) cells
double Cimt_eff = (Cic_imt > 0) ? Cic_imt : 0;
double E_imt = Emax_imt * pow(Cimt_eff, Hill_imt) /
               (pow(IC50_imt, Hill_imt) + pow(Cimt_eff, Hill_imt));

// Dasatinib effect on sensitive cells
double Cdas_eff = (Cic_das > 0) ? Cic_das : 0;
double E_das = Emax_das * pow(Cdas_eff, Hill_das) /
               (pow(IC50_das, Hill_das) + pow(Cdas_eff, Hill_das));

// Nilotinib effect on sensitive cells
double Cnil_eff = (Cic_nil > 0) ? Cic_nil : 0;
double E_nil = Emax_nil * pow(Cnil_eff, Hill_nil) /
               (pow(IC50_nil, Hill_nil) + pow(Cnil_eff, Hill_nil));

// Asciminib effect on sensitive cells (40 mg BID)
double Casc_eff = (Cp_asc > 0) ? Cp_asc : 0;
double E_asc = Emax_asc * pow(Casc_eff, Hill_asc) /
               (pow(IC50_asc, Hill_asc) + pow(Casc_eff, Hill_asc));

// Asciminib effect on T315I resistant cells (200 mg BID — higher IC50)
double E_asc_r = Emax_asc_r * pow(Casc_eff, Hill_asc) /
                 (pow(IC50_asc_r, Hill_asc) + pow(Casc_eff, Hill_asc));

// Combined TKI effect (additive, capped at 0.98)
double E_total_sens = fmin(0.98, E_imt + E_das + E_nil + E_asc);
double E_total_res  = fmin(0.98, E_asc_r);  // Only asciminib (high dose) hits T315I

// ============================================================
// Density-dependent competition (Lotka-Volterra type)
// ============================================================
double Ntotal = x0q + x0p + x1 + x2 + x3 + xs + xsp + xr + xrp +
                y0 + y1 + y2 + y3;
double suppress = fmax(0.0, 1.0 - Kcomp * Ntotal);

// ============================================================
// Normal Hematopoiesis ODEs
// ============================================================
// y0: HSC — self-renewal + input - differentiation - death
dxdt_y0 = ay0 * y0 + suppress * 0.0 - by0 * y0 - dy0 * y0;
dxdt_y1 = c1 * by0 * y0 - by1 * y1 - dy1 * y1;
dxdt_y2 = c2 * by1 * y1 - by2 * y2 - dy2 * y2;
dxdt_y3 = c3 * by2 * y2 - dy3 * y3;

// ============================================================
// Leukemic Hierarchy ODEs (total CML pool: xs+xr tracking separately)
// ============================================================

// LSC quiescent: activated by TKI pressure → quiescence; killed by NK
double q_in  = q_dorm * x0p;   // cycling→quiescent (increases under TKI)
double q_out = q_act  * x0q;   // quiescent→cycling
