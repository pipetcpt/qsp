// Initial conditions — PK depots empty
GUT_PROP_0 = 0; CENT_PROP_0 = 0; PERIPH_PROP_0 = 0;
GUT_NAD_0  = 0; CENT_NAD_0  = 0; PERIPH_NAD_0  = 0;
GUT_MEX_0  = 0; CENT_MEX_0  = 0;
GUT_KCL_0  = 0; CENT_KCL_0  = 0;
GUT_SPIRO_0= 0; CENT_SPIRO_0= 0;
GUT_QTX_0  = 0; CENT_QTX_0  = 0;

SERUM_K_0 = SERUM_K_BASE;

// Genotype-specific baseline channel conductances
double gks0 = (GENOTYPE==1) ? GKS_LQT1 : 1.0;
double gkr0 = (GENOTYPE==2) ? GKR_LQT2 : 1.0;
double gnalate0 = (GENOTYPE==3) ? GNALATE_LQT3_MULT : 1.0;
GKS_IDX_0 = gks0;
GKR_IDX_0 = gkr0;
GNALATE_IDX_0 = gnalate0;

SYMP_DRIVE_0 = 0.15;   // resting low-level sympathetic tone
QTC_0 = BASE_QTC;
EAD_SUBSTRATE_0 = 0.05;
TDP_HAZARD_0 = 0;
TDP_EVENTS_0 = 0;

[ODE]
// ---------- Beta-blocker PK: propranolol (2-cpt) ----------
double CP_PROP = CENT_PROP / V1_PROP;
dxdt_GUT_PROP    = -KA_PROP * GUT_PROP;
dxdt_CENT_PROP   =  F_PROP*KA_PROP*GUT_PROP - (CL_PROP/V1_PROP)*CENT_PROP - (Q_PROP/V1_PROP)*CENT_PROP + (Q_PROP/V2_PROP)*PERIPH_PROP;
dxdt_PERIPH_PROP =  (Q_PROP/V1_PROP)*CENT_PROP - (Q_PROP/V2_PROP)*PERIPH_PROP;

// ---------- Beta-blocker PK: nadolol (2-cpt, renal) ----------
double CP_NAD = CENT_NAD / V1_NAD;
dxdt_GUT_NAD    = -KA_NAD * GUT_NAD;
dxdt_CENT_NAD   =  F_NAD*KA_NAD*GUT_NAD - (CL_NAD/V1_NAD)*CENT_NAD - (Q_NAD/V1_NAD)*CENT_NAD + (Q_NAD/V2_NAD)*PERIPH_NAD;
dxdt_PERIPH_NAD =  (Q_NAD/V1_NAD)*CENT_NAD - (Q_NAD/V2_NAD)*PERIPH_NAD;

// ---------- Mexiletine PK (1-cpt, CYP2D6 [polymorphic] + CYP1A2) ----------
double cl_mex_2d6_now = PM_CYP2D6==1 ? CL_MEX_CYP2D6*0.30 : CL_MEX_CYP2D6;
double CL_MEX_TOTAL = cl_mex_2d6_now + CL_MEX_CYP1A2;
double CP_MEX = CENT_MEX / V_MEX;
dxdt_GUT_MEX  = -KA_MEX * GUT_MEX;
dxdt_CENT_MEX =  F_MEX*KA_MEX*GUT_MEX - (CL_MEX_TOTAL/V_MEX)*CENT_MEX;

// ---------- Oral KCl -> serum K+ turnover ----------
double CP_KCL = CENT_KCL / V_KCL;
dxdt_GUT_KCL  = -KA_KCL * GUT_KCL;
dxdt_CENT_KCL =  F_KCL*KA_KCL*GUT_KCL - (CL_KCL/V_KCL)*CENT_KCL;

// ---------- Spironolactone PK ----------
double CP_SPIRO = CENT_SPIRO / V_SPIRO;
dxdt_GUT_SPIRO  = -KA_SPIRO * GUT_SPIRO;
dxdt_CENT_SPIRO =  F_SPIRO*KA_SPIRO*GUT_SPIRO - (CL_SPIRO/V_SPIRO)*CENT_SPIRO;

// ---------- QT-prolonging challenge drug X PK ----------
double CP_QTX = CENT_QTX / V_QTX;
dxdt_GUT_QTX  = -KA_QTX * GUT_QTX;
dxdt_CENT_QTX =  F_QTX*KA_QTX*GUT_QTX - (CL_QTX/V_QTX)*CENT_QTX;

// ---------- Serum potassium (KCl intake + spironolactone retention, turnover to baseline) ----------
double spiro_k_fx = EMAX_SPIRO_K * CP_SPIRO/(EC50_SPIRO + CP_SPIRO);
double k_target = SERUM_K_BASE + EFFECT_KCL_PER_MG*CENT_KCL/KOUT_K + spiro_k_fx;
dxdt_SERUM_K = KOUT_K * (k_target - SERUM_K);

// ---------- Beta-blockade fractional sympathetic-response suppression ----------
double bb_symp_fx = 1.0 - (1.0 - CP_PROP/(EC50_PROP_SYMP+CP_PROP)) * (1.0 - CP_NAD/(EC50_NAD_SYMP+CP_NAD));
if (bb_symp_fx > 0.95) bb_symp_fx = 0.95;

// ---------- Trigger-driven sympathetic drive target (genotype x trigger-type sensitivity) ----------
double trig_mult;
if (TRIGGER==1) {          // exercise
  trig_mult = (GENOTYPE==1) ? TRIG_SENS_LQT1_EX : TRIG_CROSS_ATTEN;
} else if (TRIGGER==2) {   // auditory/emotion
  trig_mult = (GENOTYPE==2) ? TRIG_SENS_LQT2_AUD : TRIG_CROSS_ATTEN;
} else if (TRIGGER==3) {   // sleep/bradycardia -> LOW sympathetic tone is the trigger for LQT3
  trig_mult = 0.05;
} else {
  trig_mult = 0.15;        // resting baseline
}
double lcsd_fx = (LCSD_DONE==1) ? (1.0 - 0.0) : 1.0;  // LCSD reduces response to a given central drive (applied below)
double symp_target = SYMP_TRIGGER_GAIN * trig_mult * (1.0 - bb_symp_fx);
if (LCSD_DONE==1) symp_target = symp_target * 0.30;   // LCSD blunts peripheral NE release/response ~70%
dxdt_SYMP_DRIVE = KOUT_SYMP * (symp_target - SYMP_DRIVE);

// ---------- Genotype-specific channel conductance dynamics (drug-modulated) ----------
// LQT3: mexiletine blocks late-INa; propranolol contributes minor late-INa block
double mex_lateina_block = EMAX_MEX_LATEINA * CP_MEX/(EC50_MEX_LATEINA+CP_MEX);
double prop_lateina_block = EMAX_PROP_INA * CP_PROP/(EC50_PROP_INA+CP_PROP);
double lateina_block_tot = 1.0 - (1.0-mex_lateina_block)*(1.0-prop_lateina_block);
if (lateina_block_tot > 0.95) lateina_block_tot = 0.95;
double gnalate_target = gnalate0 * (1.0 - lateina_block_tot);
dxdt_GNALATE_IDX = 0.35 * (gnalate_target - GNALATE_IDX);

// hERG/IKr: baseline genotype deficit, acquired block from drug X (CiPA), rescued somewhat by serum K+
double qtx_herg_block = EMAX_QTX_HERG * pow(CP_QTX,HILL_QTX) / (pow(EC50_QTX_HERG,HILL_QTX) + pow(CP_QTX,HILL_QTX));
double k_rescue_fx = (SERUM_K - 4.0) * 0.05;  // small conductance rescue per mEq/L above 4.0 (bounded below)
double gkr_target = gkr0 * (1.0 - qtx_herg_block) * (1.0 + k_rescue_fx);
if (gkr_target < 0.05) gkr_target = 0.05;
if (gkr_target > 1.2)  gkr_target = 1.2;
dxdt_GKR_IDX = 0.40 * (gkr_target - GKR_IDX);

// IKs: baseline genotype deficit; sympathetic "safety valve" augments IKs in NORMAL/LQT2/LQT3,
// but the augmentation mechanism itself is defective in LQT1 (captured via QTC block below, not here)
double gks_target = gks0 * (1.0 + 0.25*SYMP_DRIVE);
dxdt_GKS_IDX = 0.40 * (gks_target - GKS_IDX);

// ---------- QTc surrogate (lumped repolarization-reserve turnover model) ----------
double iks_deficit = 1.0 - GKS_IDX;
if (iks_deficit < 0) iks_deficit = 0;
double ikr_deficit = 1.0 - GKR_IDX;
if (ikr_deficit < 0) ikr_deficit = 0;
double lateina_excess = GNALATE_IDX - 1.0;
if (lateina_excess < 0) lateina_excess = 0;

// LQT1 fails to shorten QTc appropriately during sympathetic surge (failed IKs augmentation) -> paradoxical prolongation
double lqt1_failed_valve = (GENOTYPE==1) ? QTC_GAIN_SYMP_LQT1 * SYMP_DRIVE : 0.0;

double qtc_target = QTC_WT_BASE
                     + QTC_GAIN_IKS*iks_deficit
                     + QTC_GAIN_IKR*ikr_deficit
                     + QTC_GAIN_LATEINA*lateina_excess
                     + lqt1_failed_valve
                     - QTC_K_SHORTEN*(SERUM_K - 4.0);
if (qtc_target < 340) qtc_target = 340;
dxdt_QTC = KOUT_QTC * (qtc_target - QTC);

// ---------- EAD-probability / arrhythmogenic substrate ----------
double qtc_excess = QTC - EAD_QTC_THRESH;
if (qtc_excess < 0) qtc_excess = 0;
double symp_ead_gain = 0.0;
if (GENOTYPE==1) symp_ead_gain = EAD_SYMP_GAIN_LQT1 * SYMP_DRIVE;
else if (GENOTYPE==2) symp_ead_gain = EAD_SYMP_GAIN_LQT2 * SYMP_DRIVE;
else if (GENOTYPE==3) symp_ead_gain = EAD_BRADY_GAIN_LQT3 * (1.0 - SYMP_DRIVE);  // low sympathetic/bradycardia trigger
double ead_target = EAD_QTC_GAIN*qtc_excess + symp_ead_gain;
if (ead_target > 1.0) ead_target = 1.0;
dxdt_EAD_SUBSTRATE = KOUT_EAD * (ead_target - EAD_SUBSTRATE);

// ---------- Cumulative TdP-risk hazard & expected event count (time-to-event) ----------
double hazard_base = (GENOTYPE==1) ? HAZARD_BASE_LQT1 : ((GENOTYPE==2) ? HAZARD_BASE_LQT2 : HAZARD_BASE_LQT3);
double qtc_hazard_excess = QTC - 500.0;
if (qtc_hazard_excess < 0) qtc_hazard_excess = 0;
double female_mult = (GENOTYPE==2 && FEMALE==1) ? HAZARD_FEMALE_LQT2_MULT : 1.0;
double syncope_mult = (PRIOR_SYNCOPE==1) ? HAZARD_PRIOR_SYNCOPE_MULT : 1.0;
double lcsd_hazard_mult = (LCSD_DONE==1) ? HAZARD_LCSD_MULT : 1.0;
double hazard_rate = hazard_base * (1.0 + HAZARD_EAD_GAIN*EAD_SUBSTRATE) *
                     (1.0 + HAZARD_QTC_GAIN*qtc_hazard_excess) *
                     female_mult * syncope_mult * lcsd_hazard_mult;
dxdt_TDP_HAZARD = hazard_rate;
dxdt_TDP_EVENTS = hazard_rate;   // expected event-count accumulation (Poisson intensity)

