repolarization-reserve/QTc surrogate, EAD-substrate generation, autonomic
(sympathetic) triggering, and cumulative Torsades de Pointes (TdP) risk
hazard/event probability, with mechanistic PK/PD for propranolol, nadolol,
mexiletine, potassium/spironolactone, a generic QT-prolonging "challenge
drug X" (acquired-on-congenital CiPA-style hERG block), plus categorical
