##  # ---------- Calibration anchors ----------
##  # Imatinib exquisite sensitivity in FIP1L1-PDGFRA+ HES (Cools 2003 NEJM;
##  #   Pardanani 2003 Blood): complete hematologic response within 1-4 weeks
##  #   at doses as low as 100 mg/day; molecular remission (transcript
##  #   undetectable) typically follows over months.
##  # T674I resistance mutation (Cools 2004 Cancer Cell): analogous to BCR-ABL
##  #   T315I, confers >10-15x rightward shift in imatinib EC50; clinically
##  #   managed by alternative TKI or cytoreductive/biologic therapy.
##  # Mepolizumab HES trials (Roufosse 2020 JACI; Kuang 2022 NEJM): reduces
##  #   flare frequency and enables steroid-sparing in steroid-dependent/
##  #   refractory idiopathic and lymphocytic-variant HES; does not directly
##  #   deplete eosinophils but blocks IL-5-driven survival/production.
##  # Benralizumab ADCC mechanism (Kolbeck 2010 JACI; Kuang 2019 JACI):
##  #   afucosylated Fc drives near-complete eosinophil depletion independent
##  #   of IL-5 pathway status, including in FIP1L1-PDGFRA-negative and
##  #   lymphocytic-variant disease.
##  # Loeffler endocarditis natural history (Parrillo 1979 Ann Intern Med):
##  #   progresses acute necrotic -> thrombotic -> fibrotic stage over weeks
##  #   to months if eosinophilia uncontrolled; fibrotic/restrictive stage is
##  #   largely irreversible even after hematologic remission is achieved.
