// ---------- PK ----------
double CIMA   = CEN_IMA   / V_IMA;
double CPRED  = CEN_PRED  / V_PRED;
double CMEPO  = CEN_MEPO  / V_MEPO;
double CBENRA = CEN_BENRA / V_BENRA;
double CHU    = CEN_HU    / V_HU;
double CRUX   = CEN_RUX   / V_RUX;

dxdt_GUT_IMA = -KA_IMA * GUT_IMA;
dxdt_CEN_IMA =  F_IMA * KA_IMA * GUT_IMA - (CL_IMA/V_IMA) * CEN_IMA;

dxdt_GUT_PRED = -KA_PRED * GUT_PRED;
dxdt_CEN_PRED =  F_PRED * KA_PRED * GUT_PRED - (CL_PRED/V_PRED) * CEN_PRED;

dxdt_DEPOT_MEPO = -KA_MEPO * DEPOT_MEPO;
dxdt_CEN_MEPO   =  F_MEPO * KA_MEPO * DEPOT_MEPO - (CL_MEPO/V_MEPO) * CEN_MEPO;

dxdt_DEPOT_BENRA = -KA_BENRA * DEPOT_BENRA;
dxdt_CEN_BENRA   =  F_BENRA * KA_BENRA * DEPOT_BENRA - (CL_BENRA/V_BENRA) * CEN_BENRA;

dxdt_GUT_HU = -KA_HU * GUT_HU;
dxdt_CEN_HU =  F_HU * KA_HU * GUT_HU - (CL_HU/V_HU) * CEN_HU;

dxdt_GUT_RUX = -KA_RUX * GUT_RUX;
dxdt_CEN_RUX =  F_RUX * KA_RUX * GUT_RUX - (CL_RUX/V_RUX) * CEN_RUX;

// ---------- Kinase-signaling index (imatinib for GENOTYPE 1/2; ruxolitinib for GENOTYPE 3) ----------
double ima_ec50 = EC50_IMA_KIN * (T674I_MUT == 1 ? T674I_EC50_MULT : 1.0);
double ima_inh  = (GENOTYPE == 1 || GENOTYPE == 2) ?
                   EMAX_IMA_KIN * CIMA / (ima_ec50 + CIMA) : 0.0;
double rux_inh  = (GENOTYPE == 3) ?
                   EMAX_RUX_KIN * CRUX / (EC50_RUX_KIN + CRUX) : 0.0;
double kin_ss   = kin_base * (1.0 - ima_inh) * (1.0 - rux_inh);
dxdt_KIN = KOUT_KIN * (kin_ss - KIN);

// ---------- Endogenous IL-5 index (corticosteroid-suppressible) ----------
double pred_il5_inh = EMAX_PRED_IL5 * CPRED / (EC50_PRED_IL5 + CPRED);
double il5_ss = il5_base * (1.0 - pred_il5_inh);
dxdt_IL5 = KOUT_IL5 * (il5_ss - IL5);

// free (mepolizumab-neutralizable) IL-5 signal reaching the marrow
double mepo_neut = MEPO_IL5_NEUT_EMAX * CMEPO / (EC50_MEPO_IL5 + CMEPO);
double IL5_free = IL5 * (1.0 - mepo_neut);

// ---------- Marrow eosinophil-production drive ----------
double hu_suppress = HU_KIN_SUPPRESS * CHU / (2.0 + CHU);
double marrow_ss = (MARROW_GAIN_KIN * KIN + MARROW_GAIN_IL5 * IL5_free) * (1.0 - hu_suppress);
dxdt_MARROW = KOUT_MARROW * (marrow_ss - MARROW);

// ---------- Circulating absolute eosinophil count ----------
double pred_apop = PRED_DIRECT_APOPTOSIS * CPRED / (EC50_PRED_APOP + CPRED);
double benra_adcc = BENRA_ADCC_EMAX * CBENRA / (EC50_BENRA_ADCC + CBENRA);
double aec_ss = BASE_AEC * AEC_MARROW_GAIN * MARROW * (1.0 - pred_apop) * (1.0 - benra_adcc);
if (aec_ss < 10) aec_ss = 10;
dxdt_AEC = KOUT_AEC * (aec_ss - AEC);

// ---------- Tissue eosinophil burden ----------
double tissue_ss = TISSUE_AEC_GAIN * (AEC / BASE_AEC);
dxdt_TISSUE = KOUT_TISSUE * (tissue_ss - TISSUE);

// ---------- Cardiac (Loeffler) damage index ----------
double cardiac_accel = (CARDIAC_INVOLVED == 1) ? CARDIAC_ACCEL_INVOLVED : 1.0;
double cardiac_drive = CARDIAC_TISSUE_GAIN * cardiac_accel * TISSUE;
double cardiac_ss = CARDIAC_REGRESS_FLOOR + (1.0 - CARDIAC_REGRESS_FLOOR) *
                     (cardiac_drive / (cardiac_drive + KOUT_CARDIAC));
dxdt_CARDIAC = KOUT_CARDIAC * (cardiac_ss - CARDIAC);

// ---------- Cardiac troponin (tracks *active* damage-accrual rate) ----------
double cardiac_rate = fabs(KOUT_CARDIAC * (cardiac_ss - CARDIAC));
double trop_ss = TROP_BASE + TROP_GAIN_CARDIAC_RATE * cardiac_rate;
dxdt_TROP = KOUT_TROP * (trop_ss - TROP);

// ---------- Serum ECP ----------
double ecp_ss = ECP_BASE + ECP_GAIN_TISSUE * TISSUE;
dxdt_ECP = KOUT_ECP * (ecp_ss - ECP);

// ---------- Composite HES symptom/flare score ----------
double symptom_ss = SYMPTOM_GAIN_AEC * (AEC / BASE_AEC) +
                     SYMPTOM_GAIN_CARDIAC * CARDIAC +
                     SYMPTOM_GAIN_TISSUE * TISSUE;
if (symptom_ss > 10) symptom_ss = 10;
dxdt_SYMPTOM = KOUT_SYMPTOM * (symptom_ss - SYMPTOM);

