[PROB]
QSP model of hypereosinophilic syndrome (HES) linking molecular-genotype-
specific constitutive kinase signaling (FIP1L1-PDGFRA/PDGFRB/JAK2) or
clonal-T-cell IL-5 overproduction to marrow eosinophilopoiesis, circulating/
tissue eosinophil burden, degranulation-mediated cardiac (Loeffler
endocarditis) and composite organ damage, and genotype-matched pharmacologic
response (imatinib, corticosteroids, mepolizumab, benralizumab, hydroxyurea,
ruxolitinib).

