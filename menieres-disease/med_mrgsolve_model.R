[ODE]
// ---------- Betahistine PK ----------
double CBETA = CEN_BETA / V_BETA;
dxdt_GUT_BETA = -KA_BETA*GUT_BETA;
dxdt_CEN_BETA =  F_BETA*KA_BETA*GUT_BETA - (CL_BETA/V_BETA)*CEN_BETA - KA_MET*CEN_BETA;
dxdt_MET_BETA =  KA_MET*CEN_BETA - KE_MET*MET_BETA;
double CMET = MET_BETA;  // arbitrary metabolite-pool "concentration" surrogate (ng/mL-equivalent)

// ---------- Diuretic PK (lumped) ----------
double CDIUR = CEN_DIUR / V_DIUR;
dxdt_GUT_DIUR = -KA_DIUR*GUT_DIUR;
dxdt_CEN_DIUR =  F_DIUR*KA_DIUR*GUT_DIUR - (CL_DIUR/V_DIUR)*CEN_DIUR;

// ---------- Intratympanic dexamethasone PK ----------
dxdt_ME_DEX   = -KMEDEX*ME_DEX;
dxdt_PERI_DEX =  KMEDEX*ME_DEX/V_PERI_DEX - KPERI_OUT_DEX*PERI_DEX;
double GR_target = EC50_DEX_GR>0 ? PERI_DEX/(EC50_DEX_GR + PERI_DEX) : 0;
dxdt_GR_DEX   = KOUT_GRDEX*(GR_target - GR_DEX);

// ---------- Intratympanic gentamicin PK ----------
dxdt_ME_GENT   = -KMEGENT*ME_GENT;
dxdt_PERI_GENT =  KMEGENT*ME_GENT/V_PERI_GENT - KPERI_OUT_GENT*PERI_GENT;

// ---------- Drug effect fractions ----------
double beta_comp_fx = EMAX_BETA_COMP * CMET/(EC50_BETA_CBF + CMET);
double beta_hydr_fx = EMAX_BETA_HYDR * CMET/(EC50_BETA_CBF + CMET);
double diur_hydr_fx = EMAX_DIUR_HYDR * CDIUR/(EC50_DIUR + CDIUR);
double dex_antiinflam_fx = EMAX_DEX_ANTIINFLAM * GR_DEX;
double dex_hydr_fx  = EMAX_DEX_HYDR * GR_DEX;

// ---------- Endolymphatic hydrops ----------
// Production scaled by baseline stage severity (stage_mult, fixed at t=0 in $MAIN);
// resorption is first-order, and drugs act by suppressing production (betahistine,
// diuretic) or improving resorption (intratympanic dexamethasone, folded into the
// combined production-suppression term below for simplicity).
double hydr_in = KIN_HYDROPS * stage_mult * (1.0 - beta_hydr_fx) * (1.0 - diur_hydr_fx) * (1.0 - dex_hydr_fx);
dxdt_HYDROPS = hydr_in - KOUT_HYDROPS*HYDROPS;

// ---------- Episodic Reissner's-membrane rupture -> K+ intoxication ----------
double hydr_pow   = pow(HYDROPS, HILL_RUPT);
double hydr50_pow = pow(HYDROPS_RUPT50, HILL_RUPT);
double rupture_rate = KRUPTURE_BASE * hydr_pow/(hydr50_pow + hydr_pow) * (1.0 - 0.5*dex_antiinflam_fx);
dxdt_KINTOX = KIN_KINTOX_PER_RUPT*rupture_rate - KOUT_KINTOX*KINTOX;

// ---------- Cochlear (OHC) viability ----------
dxdt_OHC = -( KOUT_OHC + K_KINTOX_OHC*KINTOX*(1.0 - dex_antiinflam_fx) )*OHC
           - KKILL_COCH_MAX*(PERI_GENT/(EC50_GENT_COCHTOX+PERI_GENT))*OHC
           + KREGEN_OHC*(1.0 - OHC);

// ---------- Vestibular hair-cell viability ----------
dxdt_VEST = -( KOUT_VEST + K_KINTOX_VEST*KINTOX*(1.0 - dex_antiinflam_fx) )*VEST
            - KKILL_VEST_MAX*(PERI_GENT/(EC50_GENT_VESTKILL+PERI_GENT))*VEST
            + KREGEN_VEST*(1.0 - VEST);

// ---------- Vertigo attack-frequency state (turnover toward mechanistic target) ----------
double vert_target = W_HYDR_VERT*(HYDROPS-1.0) + W_KINTOX_VERT*KINTOX + W_VEST_VERT*(1.0-VEST) - 10.0*COMP;
if (vert_target < 0) vert_target = 0;
if (vert_target > VERTFREQ_MAX) vert_target = VERTFREQ_MAX;
dxdt_VERTFREQ = (vert_target - VERTFREQ) / TAU_VERTFREQ;

// ---------- Central vestibular compensation ----------
dxdt_COMP = KIN_COMP*(1.0 + beta_comp_fx) - KOUT_COMP*COMP - K_ATTACK_DECOMP*0.001*VERTFREQ*COMP;

// ---------- Dizziness Handicap Inventory (turnover toward target) ----------
double tumarkin_flag = (VEST < TUMARKIN_VEST_THRESH && HYDROPS > TUMARKIN_HYDR_THRESH) ? 1.0 : 0.0;
double dhi_target = 20 + W_VERT_DHI*VERTFREQ + W_TUMARKIN_DHI*tumarkin_flag + W_COMP_DHI*COMP;
if (dhi_target < 0) dhi_target = 0;
if (dhi_target > DHI_MAX) dhi_target = DHI_MAX;
dxdt_DHI = (dhi_target - DHI) / 300.0;

