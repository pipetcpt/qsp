##  # ---------- Calibration anchors ----------
##  # Betahistine (Nauta 2014 Eur Arch Otorhinolaryngol meta-analysis): modest
##  #   overall reduction in vertigo frequency/severity across pooled small
##  #   RCTs, substantial heterogeneity; BEMED trial (Adrion 2016 BMJ,
##  #   PMID 27075667) - high-dose betahistine (48mg TID x9mo) showed NO
##  #   significant superiority over placebo on definitive vertigo-day rate in
##  #   a well-powered double-blind RCT - captured here via a deliberately
##  #   small EMAX_BETA_COMP/EMAX_BETA_HYDR effect size (mechanistic plausibility
##  #   via cochlear blood flow / vestibular compensation, but limited RCT-
##  #   confirmed magnitude on hard endpoints).
##  # Diuretics (James AL, Burton MJ. Cochrane Database Syst Rev 2001; Thirlwall
##  #   AS, Kundu S. Cochrane Database Syst Rev 2006) - long-standing clinical
##  #   use but limited/low-quality RCT evidence; modeled with a modest
##  #   EMAX_DIUR_HYDR effect reflecting plausible-but-unproven fluid benefit.
##  # Intratympanic steroids (Phillips JS, Westerberg B. Cochrane Database Syst
##  #   Rev 2011;(7):CD008514) - some evidence for short/medium-term vertigo
##  #   control, generally favorable safety profile vs gentamicin (no hearing-
##  #   loss risk) - modeled via dex_antiinflam_fx reducing rupture rate and
##  #   K+-intoxication-driven hair-cell injury.
##  # Intratympanic gentamicin (Patel M, et al. Lancet 2016;388(10061):2753-2762,
##  #   PMID 27544377 - low-dose titration vs single high fixed dose: equivalent
