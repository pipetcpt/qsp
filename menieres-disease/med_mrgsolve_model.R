[PROB]
QSP model of Ménière's disease linking endolymphatic sac dysfunction and
stria vascularis ion-transport imbalance to endolymphatic hydrops, episodic
Reissner's membrane rupture/perilymph K+ intoxication, cochlear and
vestibular hair-cell injury, and the resulting episodic vertigo, fluctuating
hearing loss, tinnitus and handicap, with mechanistic PK/PD for betahistine,
thiazide/K+-sparing diuretic, intratympanic dexamethasone and intratympanic
gentamicin (chemical labyrinthectomy).

