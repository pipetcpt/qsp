##  # ---------- Calibration anchors ----------
##  # MOR occupancy / respiratory Hill relationship (Dahan 2005/2010): steep,
##  #   near-threshold suppression of hypoxic ventilatory response at high
##  #   full-agonist occupancy; buprenorphine plateaus ~40-50% max suppression
##  #   even at supratherapeutic doses (Walsh 1994; Greenwald 2003 PET occupancy)
##  # Naloxone renarcotization (t1/2 30-90 min vs fentanyl/methadone t1/2 hours):
##  #   repeat dosing/infusion required, esp. for long-acting agonist overdose
##  # Methadone QTc (Krantz 2009; Chou 2014 ASAM/ACP guideline): dose-dependent
##  #   hERG block, torsades risk rises sharply >120-150 mg/day or with
