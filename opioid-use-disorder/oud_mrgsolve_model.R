// ---------- PK ----------
double CFENT1 = CEN_FENT / V1_FENT;
double CFENT2 = PER_FENT / V2_FENT;
double CMETH  = CEN_METH / V_METH;
double CBUP   = CEN_BUP  / V_BUP;
double CNLX   = CEN_NLX  / V_NLX;
double CNTX   = CEN_NTX  / V_NTX;
double CLOF   = CEN_LOF  / V_LOF;

dxdt_GUT_FENT = -KA_FENT_IN * GUT_FENT;
dxdt_CEN_FENT =  F_FENT_IN * KA_FENT_IN * GUT_FENT
                 - (CL_FENT/V1_FENT) * CEN_FENT
                 - (Q_FENT/V1_FENT) * CEN_FENT + (Q_FENT/V2_FENT) * PER_FENT;
dxdt_PER_FENT =  (Q_FENT/V1_FENT) * CEN_FENT - (Q_FENT/V2_FENT) * PER_FENT;

dxdt_GUT_METH = -KA_METH * GUT_METH;
dxdt_CEN_METH =  F_METH * KA_METH * GUT_METH - (CL_METH/V_METH) * CEN_METH;

dxdt_GUT_BUP    = -KA_BUP_SL * GUT_BUP;
dxdt_DEPOT_BUP_XR = -KREL_BUP_XR * DEPOT_BUP_XR;
dxdt_CEN_BUP    =  F_BUP_SL * KA_BUP_SL * GUT_BUP + KREL_BUP_XR * DEPOT_BUP_XR
                   - (CL_BUP/V_BUP) * CEN_BUP;

dxdt_GUT_NLX = -KA_NLX * GUT_NLX;
dxdt_CEN_NLX =  F_NLX_IN * KA_NLX * GUT_NLX - (CL_NLX/V_NLX) * CEN_NLX;

dxdt_GUT_NTX    = -KA_NTX_PO * GUT_NTX;
dxdt_DEPOT_NTX_XR = -KREL_NTX_XR * DEPOT_NTX_XR;
dxdt_CEN_NTX    =  F_NTX_PO * KA_NTX_PO * GUT_NTX + KREL_NTX_XR * DEPOT_NTX_XR
                   - (CL_NTX/V_NTX) * CEN_NTX;

dxdt_GUT_LOF = -KA_LOF * GUT_LOF;
dxdt_CEN_LOF =  F_LOF * KA_LOF * GUT_LOF - (CL_LOF/V_LOF) * CEN_LOF;

// ---------- MOR occupancy: competitive-antagonism / multi-ligand Emax ----------
// Convert plasma "mg/L" to ng/mL-equivalent (numerically identical) for Ki comparison.
double a_fent = CFENT1 / KI_FENT;
double a_meth = CMETH  / KI_METH;
double a_bup  = CBUP   / KI_BUP;
double a_nlx  = CNLX   / KI_NLX;
double a_ntx  = CNTX   / KI_NTX;
double sum_a  = a_fent + a_meth + a_bup + a_nlx + a_ntx;
double occ_total = sum_a / (1.0 + sum_a);           // total fractional MOR occupancy (all ligands)
double occ_agonist_frac = (sum_a > 1e-9) ? (a_fent + a_meth + a_bup) / sum_a : 0.0;
// Effective agonist-weighted occupancy (partial agonist ceiling applied to buprenorphine share)
double occ_full   = (sum_a > 1e-9) ? occ_total * ((a_fent*EFF_FENT + a_meth*EFF_METH + a_bup*EFF_BUP) / sum_a) : 0.0;
double occ_eff = occ_full;
if (occ_eff > BUP_CEILING_OCC && (a_bup > a_fent) && (a_bup > a_meth)) {
  // buprenorphine-dominated regime: enforce partial-agonist ceiling
  occ_eff = BUP_CEILING_OCC + (occ_eff - BUP_CEILING_OCC) * 0.15;
}

// ---------- Tolerance/adaptation (slow; driven by sustained effective occupancy) ----------
double TOL_target = TOL_DRIVE_GAIN * occ_eff + BASE_TOL * (1.0 - occ_eff*0.3);
if (TOL_target > 1.0) TOL_target = 1.0;
dxdt_TOL = KOUT_TOL * (TOL_target - TOL);

// ---------- Locus-coeruleus tone (opponent-process rebound on occupancy deficit) ----------
double occ_deficit = TOL - occ_eff;                 // positive when tolerance exceeds current occupancy (withdrawal state)
if (occ_deficit < 0) occ_deficit = 0;
double lof_fx = EMAX_LOF_LC * CLOF / (EC50_LOF_LC + CLOF);
double LC_target = 1.0 + LC_REBOUND_GAIN * occ_deficit * (1.0 - lof_fx);
dxdt_LC = KOUT_LC * (LC_target - LC);

// ---------- Withdrawal (COWS) ----------
double lc_excess = LC - 1.0;
if (lc_excess < 0) lc_excess = 0;
double COWS_target = SCALE_COWS_LC * lc_excess;
if (COWS_target > 48) COWS_target = 48;
dxdt_COWS = KOUT_COWS * (COWS_target - COWS);

// ---------- Craving ----------
double counsel_fx = (COUNSELING > 0) ? (1.0 - COUNSELING_CRAVE_RED) : 1.0;
double CRAVE_target = (SCALE_CRAVE_COWS * COWS + SCALE_CRAVE_TOL * TOL) * counsel_fx;
if (CRAVE_target > 100) CRAVE_target = 100;
if (CRAVE_target < 0) CRAVE_target = 0;
dxdt_CRAVE = KOUT_CRAVE * (CRAVE_target - CRAVE);

// ---------- Respiratory drive (Hill suppression by full-agonist occupancy; synergy/adulterant terms) ----------
double occ_h = pow(occ_eff, RESP_GAMMA);
double resp_supp = RESP_EMAX_MOR * occ_h / (pow(0.5, RESP_GAMMA) + occ_h);
double benzo_fx = (BENZO_COUSE > 0) ? BENZO_RESP_MULT : 1.0;
double xyl_fx = (XYLAZINE_ADULT > 0) ? XYLAZINE_RESP_ADD : 0.0;
double RESP_target = 1.0 - resp_supp*benzo_fx - xyl_fx;
if (RESP_target < 0) RESP_target = 0;
dxdt_RESP = KOUT_RESP * (RESP_target - RESP);

// ---------- Methadone QTc ----------
double meth_qtc_fx = EMAX_METH_QTC * CMETH / (EC50_METH_QTC + CMETH);
double QTC_target = QTC_BASE + meth_qtc_fx;
dxdt_QTC = KOUT_QTC * (QTC_target - QTC);

// ---------- Phasic dopamine / euphoria (driven by newly acquired occupancy, blunted by tolerance) ----------
double new_occ = occ_eff - TOL;
if (new_occ < 0) new_occ = 0;
double DA_target = DA_GAIN_NEWOCC * new_occ * (1.0 - 0.6*TOL);
if (DA_target > 100) DA_target = 100;
dxdt_DA = KOUT_DA * (DA_target - DA);

// ---------- Treatment retention ----------
int on_maintenance = (a_bup > 0.05 || CMETH > (5.0/V_METH) || CNTX > (1.0/V_NTX)) ? 1 : 0;
double counsel_bonus = (COUNSELING > 0) ? RETAIN_COUNSEL_BONUS : 0.0;
double RETAIN_target = RETAIN_TARGET_BASE + (on_maintenance ? RETAIN_MAT_BONUS : 0.0)
                        - RETAIN_COWS_PENALTY*COWS + counsel_bonus;
if (RETAIN_target > 1.0) RETAIN_target = 1.0;
if (RETAIN_target < 0) RETAIN_target = 0;
dxdt_RETAIN = KOUT_RETAIN * (RETAIN_target - RETAIN);

