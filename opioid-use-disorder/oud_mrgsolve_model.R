[PROB]
QSP model of opioid use disorder (OUD) linking competitive mu-opioid-receptor
(MOR) occupancy from illicit fentanyl, methadone, buprenorphine, naloxone, and
naltrexone to chronic-tolerance/withdrawal neuroadaptation (locus-coeruleus
noradrenergic rebound), craving/relapse risk, dose-dependent respiratory
depression/overdose, methadone-associated QTc prolongation, and treatment-
retention outcomes, with symptomatic alpha2-agonist (lofexidine) withdrawal
management.

