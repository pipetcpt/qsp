##  Phenylketonuria (페닐케톤뇨증, PKU) — QSP / PK-PD Model
##  ============================================================================
##  Disease  : PKU — autosomal-recessive PAH-gene loss-of-function causing
##             hepatic phenylalanine hydroxylase deficiency, hyperphenyl-
##             alaninemia, LAT1/BBB large-neutral-amino-acid transport
##             competition, cerebral monoamine (dopamine/serotonin) synthesis
##             deficit and neurotoxicity (classic / moderate / mild PKU and
##             mild hyperphenylalaninemia, MHP, across the residual-activity
##             spectrum; BH4-responsive vs non-responsive genotypes).
##  Authors  : Generated by Claude Code Routine (QSP Disease Model Library)
##  Engine   : mrgsolve (>=1.5)
##
##  Scope    : 18 ODE compartments — drug PK (sapropterin/BH4 2-cmpt oral,
##             pegvaliase SC 2-cmpt + anti-drug-antibody titer + immune-
##             tolerization state) + amino-acid mass-balance network (plasma
##             Phe/Tyr/Trp via Michaelis-Menten hepatic PAH + pegvaliase
##             alternative clearance + linear minor-pathway/renal clearance;
##             brain Phe/Tyr/Trp via saturable, competitively-inhibited LAT1
##             transport) + downstream PD (dopamine- and serotonin-synthesis
##             indices, myelin/white-matter integrity, cumulative IQ deficit
##             weighted by a critical-developmental-period function of age,
##             executive-function index, growth Z-score).
##  Time     : hours (drug PK, diurnal Phe fluctuation and BBB transport
##             equilibrate over hours; ADA/tolerization over weeks; myelin/
##             growth over months; cumulative IQ deficit over years via a
##             critical-period-weighted, largely irreversible accrual term).
##             Default simulation horizon 0 - 70,080 h (8 years) for natural-
##             history/childhood scenarios; shorter horizons for PK-focused runs.
##  Units    : dose in mg; plasma/brain Phe-Tyr-Trp in umol/L; drug plasma
##             concentration in mg/L; ADA/tolerization/dopamine-serotonin/
##             myelin as relative indices; IQ deficit in points below genetic
##             potential; executive-function index and growth Z-score as
##             illustrative composite scores.
##  Scenarios: 10 prebuilt — see PKU_simulate_scenarios() at bottom.
##  Anchors  : Levy 2007 Lancet (sapropterin PKU-001 phase 3, PMID 17693179);
##             Trefz 2009 J Pediatr (sapropterin PKU-003 pediatric, PMID
##             19261295); Thomas 2018 Mol Genet Metab (pegvaliase PRISM-1/2
##             phase 3 induction/titration, PMID 29653686); Longo 2018
##             Orphanet J Rare Dis (pegvaliase PAL-003 long-term extension,
##             PMID 29973227); Gupta 2018 EBioMedicine (pegvaliase pooled
##             immunogenicity/ADA kinetics, PMID 30366815); Vockley 2014 Genet
##             Med (ACMG PKU management guideline, target Phe 120-360 umol/L
##             lifelong, PMID 24385074); Rouse 2000 J Pediatr / Koch 2000 Mol
##             Genet Metab (Maternal PKU Collaborative Study, congenital heart
