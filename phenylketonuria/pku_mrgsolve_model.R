[PROB]
QSP model of phenylketonuria (PKU) linking dietary phenylalanine intake,
saturable hepatic PAH/BH4 enzymatic clearance, saturable BBB LAT1 amino-acid
transport competition, cerebral monoamine synthesis and myelination, and
critical-period-weighted cumulative neurocognitive injury, with mechanistic
PK/PD for sapropterin dihydrochloride (BH4 cofactor) and pegvaliase (PEG-PAL
enzyme substitution, including anti-drug-antibody immunogenicity and
tolerization) plus dietary/medical-formula management.

