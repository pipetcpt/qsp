[ODE]
// ---------- Drug PK ----------
double CBH4 = CEN_BH4 / V_BH4;
double CPEG = CEN_PEG / V_PEG;

dxdt_GUT_BH4 = -KA_BH4 * GUT_BH4;
dxdt_CEN_BH4 =  F_BH4 * KA_BH4 * GUT_BH4 - (CL_BH4/V_BH4) * CEN_BH4;

double ada_neutral_frac = (ADA/(ADA+ADA50)) * (1.0 - TOL);
double ada_extra_cl = 0.05 * ADA;  // ADA-accelerated PEG-PAL clearance
dxdt_SC_PEG  = -KA_PEG * SC_PEG;
dxdt_CEN_PEG =  F_PEG * KA_PEG * SC_PEG - (CL_PEG/V_PEG + ada_extra_cl) * CEN_PEG;

double ada_drive = KIN_ADA * CPEG/(KM_ADA + CPEG);
dxdt_ADA = ada_drive - KOUT_ADA * ADA;
dxdt_TOL = KOUT_TOL * (1.0 - TOL);

// ---------- Genotype residual PAH activity & BH4 chaperone/cofactor effect ----------
double resid_frac = (GENOTYPE==0) ? RESID_CLASSIC :
                    (GENOTYPE==1) ? RESID_MODERATE :
                    (GENOTYPE==2) ? RESID_MILD : RESID_MHP;
double bh4_fx = (BH4_RESPONSIVE > 0) ? (EMAX_BH4 * CBH4/(EC50_BH4 + CBH4)) : 0.0;
double vmax_pah_eff = VMAX_PAH_NORMAL_LKG * WT * resid_frac * (1.0 + bh4_fx);

// ---------- Dietary amino-acid inputs (mg/kg/d -> umol/h) ----------
double phe_input  = DIET_PHE_MGKGD * WT / 24.0 / MW_PHE * 1000.0;
double tyr_input  = TYR_DIET_MGKGD * WT / 24.0 / MW_TYR * 1000.0;
double trp_input  = TRP_DIET_MGKGD * WT / 24.0 / MW_TRP * 1000.0;

// ---------- Plasma phenylalanine mass balance ----------
double pah_flux   = vmax_pah_eff * PLASMA_PHE/(KM_PAH + PLASMA_PHE);
double peg_active = CPEG * (1.0 - ada_neutral_frac);
if (peg_active < 0) peg_active = 0;
double vmax_peg_eff = KCAT_PEG * peg_active;
double peg_flux   = vmax_peg_eff * PLASMA_PHE/(KM_PEG + PLASMA_PHE);
double minor_flux = CL_MINOR_LKG * WT * PLASMA_PHE;
double vd_phe = VD_PHE_LKG * WT;
dxdt_PLASMA_PHE = (phe_input - pah_flux - peg_flux - minor_flux) / vd_phe;

// ---------- Plasma tyrosine mass balance (PAH-derived Tyr + diet - elimination) ----------
double vd_tyr = VD_TYR_LKG * WT;
dxdt_PLASMA_TYR = (tyr_input + pah_flux - CL_TYR_LKG*WT*PLASMA_TYR) / vd_tyr;

// ---------- Plasma tryptophan mass balance ----------
double vd_trp = VD_TRP_LKG * WT;
dxdt_PLASMA_TRP = (trp_input - CL_TRP_LKG*WT*PLASMA_TRP) / vd_trp;

// ---------- Brain (BBB LAT1) transport: Phe saturable uptake; Tyr/Trp competitively inhibited ----------
double brain_phe_target = VMAX_BBB_PHE * PLASMA_PHE/(KM_BBB_PHE + PLASMA_PHE);
dxdt_BRAIN_PHE = KOUT_BBB * (brain_phe_target - BRAIN_PHE);

double phe_compet = 1.0 + PLASMA_PHE/KI_PHE_COMPET;
double brain_tyr_target = VMAX_BBB_TYR * PLASMA_TYR/(KM_BBB_TYR*phe_compet + PLASMA_TYR);
dxdt_BRAIN_TYR = KOUT_BBB * (brain_tyr_target - BRAIN_TYR);

double brain_trp_target = VMAX_BBB_TRP * PLASMA_TRP/(KM_BBB_TRP*phe_compet + PLASMA_TRP);
dxdt_BRAIN_TRP = KOUT_BBB * (brain_trp_target - BRAIN_TRP);

// ---------- Monoamine synthesis capacity indices ----------
double da_target = BRAIN_TYR / BRAIN_TYR_REF;
if (da_target > 1.2) da_target = 1.2;
dxdt_DA_IDX = KOUT_DA * (da_target - DA_IDX);

double ser_target = BRAIN_TRP / BRAIN_TRP_REF;
if (ser_target > 1.2) ser_target = 1.2;
dxdt_SER_IDX = KOUT_SER * (ser_target - SER_IDX);

// ---------- Myelin / white-matter integrity ----------
double myelin_target = 1.0 - MYELIN_MAX_LOSS * BRAIN_PHE/(BRAIN_PHE + KM_MYELIN);
dxdt_MYELIN_IDX = KOUT_MYELIN * (myelin_target - MYELIN_IDX);

// ---------- Cumulative IQ deficit (critical-period-weighted, near-irreversible) ----------
double current_age = AGE0 + SOLVERTIME/8760.0;
double phe_excess_iq = BRAIN_PHE - IQ_SAFE_BRAIN_PHE;
if (phe_excess_iq < 0) phe_excess_iq = 0;
double crit_wt = exp(-current_age/CRIT_TAU);
double iq_drive = IQ_LOSS_RATE * phe_excess_iq * crit_wt;
dxdt_IQ_DEFICIT = iq_drive * (1.0 - IQ_DEFICIT/IQ_CAP);

// ---------- Executive-function deficit (largely reversible, tracks current control) ----------
double execfx_target = EXECFX_MAX_DEFICIT * BRAIN_PHE/(KM_EXECFX + BRAIN_PHE);
dxdt_EXECFX_IDX = KOUT_EXECFX * (execfx_target - EXECFX_IDX);

// ---------- Growth Z-score deficit (Tyr/protein-balance sensitive) ----------
double tyr_deficit_frac = (TYR_NORMAL_REF - PLASMA_TYR) / TYR_NORMAL_REF;
if (tyr_deficit_frac < 0) tyr_deficit_frac = 0;
double growthz_target = GROWTHZ_MAX_DEFICIT * tyr_deficit_frac;
dxdt_GROWTHZ = KOUT_GROWTHZ * (growthz_target - GROWTHZ);

