## Prednisone daily oral dosing (split into AM/PM equivalent using once-daily)
## Dose in mg; mrgsolve expects addl + ii for multiple dosing
dose_pred <- function(dose_mg, duration_weeks, start_week = 0) {
  ## Daily dosing: ii = 24 h, addl = (duration_weeks*7 - 1)
  mrgsolve::ev(
    time = start_week * 168,   # convert weeks to hours
    amt  = dose_mg,
    cmt  = "PRED_GUT",
    ii   = 24,
    addl = pmax(0, as.integer(duration_weeks * 7) - 1)
  )
}

## MTX weekly oral dosing
dose_mtx <- function(dose_mg, duration_weeks, start_week = 0) {
  mrgsolve::ev(
    time = start_week * 168,
    amt  = dose_mg,
    cmt  = "MTX_GUT",
    ii   = 168,   # weekly = 168 h
    addl = pmax(0, as.integer(duration_weeks) - 1)
  )
}

## Prednisone taper: 40 mg x 4wk -> 20 mg x 4wk -> 10 mg x 4wk -> 5 mg x 4wk
taper_pred <- function(start_week = 0) {
  ev1 <- dose_pred(40, 4, start_week)
  ev2 <- dose_pred(20, 4, start_week + 4)
  ev3 <- dose_pred(10, 4, start_week + 8)
  ev4 <- dose_pred(5,  4, start_week + 12)
  mrgsolve::ev_seq(ev1, ev2, ev3, ev4)
}

## Simulation wrapper: run 104 weeks, return tidy data frame
run_sim <- function(model, events, param_update = list(), sim_dur_wk = 104,
                    delta_h = 6) {
  ## Update parameters if provided
  if (length(param_update) > 0) {
    model <- mrgsolve::param(model, param_update)
  }
  out <- model |>
    mrgsolve::ev(events) |>
    mrgsolve::mrgsim(end = sim_dur_wk * 168, delta = delta_h, digits = 6) |>
    as.data.frame()
  out$time_weeks <- out$time / 168
  out
}

