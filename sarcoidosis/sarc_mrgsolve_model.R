EC50_aza   :  50.0  : Azathioprine (TGN) EC50 for lymphocyte suppression
// TGN levels at therapeutic AZA dose: 200-400 pmol/8x10^8 RBC; normalized
Emax_aza   :   0.55 : Max inhibition by AZA/TGN
EC50_hcq   : 200.0  : Hydroxychloroquine EC50 for TLR/macrophage inhibition (ng/mL)
// HCQ 400 mg/d -> Csteady ~400-700 ng/mL; EC50 ~200 ng/mL
Emax_hcq   :   0.40 : Max inhibition by HCQ (primarily macrophage/IL-12)
EC50_ifx   :   1.0  : Infliximab EC50 for TNF neutralization (mcg/mL)
// Infliximab 3 mg/kg IV -> Cmax ~70 mcg/mL; trough ~1-3 mcg/mL
Emax_ifx   :   0.90 : Max TNF neutralization by infliximab

