$MAIN
//--------------------------------------------------------------------
// Derived PK rate constants
//--------------------------------------------------------------------
double Vc_pred  = Vd_pred  * WT;        // L
double Vc_predl = Vd_predl * WT;        // L
double Vc_mtx   = Vd_mtx   * WT;        // L

double k_pred_el  = CL_pred  / Vc_pred;  // 1/h
double k_predl_el = CL_predl / Vc_predl; // 1/h
double k_mtx_el   = CL_mtx   / Vc_mtx;  // 1/h

//--------------------------------------------------------------------
// Initial conditions — set from baseline params
//--------------------------------------------------------------------
_init_MAC_ACT  = MAC_ACT0;
_init_TH1      = TH1_0;
_init_TREG     = TREG0;
_init_TNF      = TNF0;
_init_IFNG     = IFNG0;
_init_IL12     = IL12_0;
_init_GRAN     = GRAN0;
_init_FIBR     = FIBR0;
_init_ACE_BM   = ACE0;
_init_CALIT    = CALIT0;
_init_SERUM_CA = CA0;
_init_SIL2R    = SIL2R0;
_init_FVC_P    = FVC_P0;

$ODE
//====================================================================
// SECTION 1: Drug PK ODEs
//====================================================================

//--------------------------------------------------------------------
// 1a. Prednisone — two-step: gut depot -> central plasma -> conversion
//    Prednisone is a prodrug; ~89% converted to prednisolone in liver.
//    Ref: Derendorf et al. (1985), Czock et al. (2005)
//--------------------------------------------------------------------
double Vc_pred_ode  = Vd_pred  * WT;
double Vc_predl_ode = Vd_predl * WT;
double Vc_mtx_ode   = Vd_mtx   * WT;

double k_pred_el_ode  = CL_pred  / Vc_pred_ode;
double k_predl_el_ode = CL_predl / Vc_predl_ode;
double k_mtx_el_ode   = CL_mtx   / Vc_mtx_ode;

// Prednisone gut depot: dose absorbed at rate ka_pred
dxdt_PRED_GUT = -ka_pred * PRED_GUT;

// Prednisone central: absorbed from gut, eliminated (partly to prednisolone)
dxdt_PRED_C  = ka_pred * PRED_GUT
              - k_pred_el_ode * PRED_C;

// Prednisolone production from prednisone conversion (active drug)
// PRED_C in mg; prednisolone formed = k_predl_conv * CL_pred * C_pred
// Concentration PREDL_C in ng/mL: scaled by 1000 * MW_ratio (~0.97) / Vc
// We track PREDL_C as mass in Vc_predl then convert for PD effect
double pred_conc_ug_mL = PRED_C / Vc_pred_ode;  // mg/L = ug/mL

// Prednisolone central compartment (mass units, mg)
// Input: conversion from prednisone; transfer from peripheral; elimination
dxdt_PREDL_C = k_predl_conv * CL_pred * pred_conc_ug_mL * Vc_predl_ode / 1000.0
              + k21_predl * PREDL_P
              - k_predl_el_ode * PREDL_C
              - k12_predl * PREDL_C;

dxdt_PREDL_P = k12_predl * PREDL_C
              - k21_predl * PREDL_P;

//--------------------------------------------------------------------
// 1b. Methotrexate — gut depot -> plasma -> intracellular polyglutamates
//     Oral MTX 10 mg/week dosed once weekly
//     Ref: Bressolle et al. (1998), Kager et al. (2014)
//--------------------------------------------------------------------
dxdt_MTX_GUT = -ka_mtx * MTX_GUT;

dxdt_MTX_C   =  ka_mtx * MTX_GUT
              - k_mtx_el_ode  * MTX_C
              - k_poly * MTX_C;

// MTX polyglutamates: slow formation, slow elimination (t1/2 ~86 h)
// Units: nmol/L; MTX_C in mg/L -> convert: MW_mtx = 454 g/mol
// nmol/L = (mg/L) / (MW/1e6) = (mg/L)*1e6/MW -> approx 1e6/454 ~ 2200
double mtx_nmol = MTX_C * 1e6 / 454.0;  // convert mg/L to nmol/L
dxdt_MTX_POLY = k_poly * mtx_nmol - k_poly_el * MTX_POLY;

//====================================================================
// SECTION 2: Drug Effect Calculations (Emax models)
//====================================================================

// Prednisolone concentration in ng/mL for PD effect
// PREDL_C is mass in central compartment (mg); Vc_predl_ode in L
// ng/mL = (mg / L) * 1e6 / 1e6 * 1000 => ug/mL*1000 => ng/mL
double PREDL_conc_ng_mL = (PREDL_C / Vc_predl_ode) * 1000.0; // ng/mL

// Clamp concentrations to be non-negative
double predl_c = (PREDL_conc_ng_mL < 0) ? 0 : PREDL_conc_ng_mL;
double mtxpg_c = (MTX_POLY < 0)         ? 0 : MTX_POLY;
double aza_c   = (AZA_CSS  < 0)         ? 0 : AZA_CSS;
double hcq_c   = (HCQ_CSS  < 0)         ? 0 : HCQ_CSS;
double ifx_c   = (IFX_CSS  < 0)         ? 0 : IFX_CSS;

// Hill function: f(C) = Emax * C^n / (EC50^n + C^n)
// Prednisolone — broad immunosuppression; primary effect on macrophages & T cells
double n = HILL_n;
double Epred = Emax_pred * pow(predl_c, n) /
               (pow(EC50_pred, n) + pow(predl_c, n) + 1e-9);

// MTX-polyglutamates — inhibit lymphocyte proliferation & cytokine release
double Emtx = Emax_mtx * pow(mtxpg_c, n) /
              (pow(EC50_mtx_poly, n) + pow(mtxpg_c, n) + 1e-9);

// AZA (constant CSS) — inhibit T cell proliferation
double Eaza = Emax_aza * pow(aza_c, n) /
              (pow(EC50_aza, n) + pow(aza_c, n) + 1e-9);

// HCQ (constant CSS) — inhibit TLR-mediated macrophage activation and IL-12
double Ehcq = Emax_hcq * pow(hcq_c, n) /
              (pow(EC50_hcq, n) + pow(hcq_c, n) + 1e-9);

// Infliximab (constant trough) — neutralize TNF
// At 3 mg/kg q8w: Cmax ~70 mcg/mL, trough ~1-3 mcg/mL
// FIRST trial (Baughman 2006): 5% improvement in FVC at 24 wks
double Eifx = Emax_ifx * pow(ifx_c, n) /
              (pow(EC50_ifx, n) + pow(ifx_c, n) + 1e-9);

// Combined lymphocyte inhibition (pred + MTX + AZA act additively on T cells)
// Cap combined effect at 0.95 to avoid complete ablation
double Elymph = 1.0 - (1.0 - Epred) * (1.0 - Emtx) * (1.0 - Eaza);
if (Elymph > 0.95) Elymph = 0.95;

// Combined macrophage inhibition (pred + HCQ)
double Emac = 1.0 - (1.0 - Epred) * (1.0 - Ehcq);
if (Emac > 0.95) Emac = 0.95;

// TNF inhibition: pred + infliximab (infliximab highly specific for TNF)
double Etnf_inhib = 1.0 - (1.0 - Epred * 0.5) * (1.0 - Eifx);
if (Etnf_inhib > 0.98) Etnf_inhib = 0.98;

//====================================================================
// SECTION 3: Disease PD ODEs
//====================================================================

// ── Clamp state variables to physiological range ─────────────────
double mac  = (MAC_ACT  < 0) ? 0 : MAC_ACT;
double th1  = (TH1      < 0) ? 0 : TH1;
double treg = (TREG     < 0) ? 0 : TREG;
double tnf  = (TNF      < 0) ? 0 : TNF;
double ifng = (IFNG     < 0) ? 0 : IFNG;
double il12 = (IL12     < 0) ? 0 : IL12;
double gran = (GRAN     < 0) ? 0 : ((GRAN > 100) ? 100 : GRAN);
double fibr = (FIBR     < 0) ? 0 : ((FIBR > 1)   ? 1   : FIBR);

// ── Regulatory suppression factor: Tregs suppress effector responses ──
// Treg effect: reduces macrophage + Th1 activity
double treg_ratio = TREG0 / (treg + 1e-9);  // ratio: elevated Tregs = protection
double treg_suppress = 1.0 / (1.0 + treg / (TREG0 * 2.0));  // sigmoidal suppression

//--------------------------------------------------------------------
// 3a. Activated macrophages (MAC_ACT)
//     Stimulated by: self (autocrine TNF), IL-12 from prior activation
//     Inhibited by: prednisolone, HCQ (TLR blockade), Tregs
//     Ref: Salgame et al. (2014) J Immunol; Iannuzzi et al. (2007) NEJM
//--------------------------------------------------------------------
// Antigen-driven persistent stimulation (mycobacterial/environmental trigger)
// In sarcoidosis: persistent antigen drives chronic macrophage activation
double mac_stim_input = k_mac_stim * (tnf / TNF0) * (il12 / IL12_0) * treg_ratio;
// Spontaneous resolution: SPON_RES flag reduces antigenic stimulation
double antigen_drive = 1.0 - SPON_RES * 0.8;  // 80% reduction for Stage I

dxdt_MAC_ACT = mac_stim_input * MAC_ACT0 * antigen_drive
              - k_mac_el * mac * (1.0 + Emac);

//--------------------------------------------------------------------
// 3b. Th1 cells
//     Stimulated by: IL-12 (critical Th1 polarization), IFN-g (amplification)
//     Inhibited by: prednisolone, MTX, AZA, Tregs
//     Ref: Müller-Quernheim (1998) Sarcoidosis Vasc Diffuse Lung Dis
//--------------------------------------------------------------------
double th1_stim = k_th1_stim * (il12 / IL12_0) * (1.0 + ifng / IFNG0);
dxdt_TH1 = th1_stim * TH1_0 * treg_suppress
           - k_th1_el * th1 * (1.0 + Elymph);

//--------------------------------------------------------------------
// 3c. Regulatory T cells (TREG)
//     Induced by: activated macrophages (via TGF-β signaling)
//     Prednisolone has complex effect: transiently spares Tregs
//     Ref: Miyara et al. (2006) J Exp Med
//--------------------------------------------------------------------
double treg_stim = k_treg_stim * mac;
dxdt_TREG = treg_stim * (1.0 + Epred * 0.2)  // pred mildly spares Tregs
           - k_treg_el * treg;

//--------------------------------------------------------------------
// 3d. TNF-alpha
//     Produced by: activated macrophages (central cytokine in sarcoid granuloma)
//     Inhibited by: prednisolone (via NF-kB), infliximab (direct neutralization)
//     Ref: Ziegenhagen & Müller-Quernheim (2003) J Intern Med
//     FIRST trial: infliximab 3 mg/kg improved FVC by ~5% at 24 wks
//--------------------------------------------------------------------
dxdt_TNF = k_tnf * mac * MAC_ACT0 / (MAC_ACT0 + mac)  // Michaelis-Menten production
          * (1.0 - Etnf_inhib)
          - k_tnf_el * tnf;

//--------------------------------------------------------------------
// 3e. IFN-gamma
//     Produced by: Th1 cells (hallmark cytokine of sarcoidosis)
//     Inhibited by: prednisolone
//     Amplifies macrophage activation -> positive feedback loop
//     Ref: Agostini et al. (1998) Eur Respir J
//--------------------------------------------------------------------
dxdt_IFNG = k_ifng * th1 * TH1_0 / (TH1_0 + th1)
           * (1.0 - Epred * 0.6)
           - k_ifng_el * ifng;

//--------------------------------------------------------------------
// 3f. IL-12
//     Produced by: activated macrophages and DCs (key Th1 polarizer)
//     Inhibited by: HCQ (blocks TLR7/9-mediated production), prednisolone
//     Ref: Minozzi et al. (2010) — HCQ reduces IL-12 in sarcoid macrophages
//--------------------------------------------------------------------
dxdt_IL12 = k_il12 * mac
           * (1.0 - Ehcq * 0.8) * (1.0 - Epred * 0.5)
           - k_il12_el * il12;

//--------------------------------------------------------------------
// 3g. Granuloma burden (GRAN)
//     Formation: driven by macrophage activation + TNF + IFN-g
//     (Multinucleated giant cell formation requires all three signals)
//     Resolution: spontaneous (slow, years) or treatment-accelerated
//     Ref: Mushaben et al. — granuloma dynamics
//     ACE-25 trial: ~25% reduction in granuloma-related ACE over 2 years
//--------------------------------------------------------------------
// Formation signal: product of key drivers (normalized)
double gran_drive = (mac / MAC_ACT0) * (tnf / TNF0) * (ifng / IFNG0);

// Net drug effect on granuloma resolution: combined suppression
double Edrug_gran = 1.0 - (1.0 - Emac) * (1.0 - Elymph * 0.5);

// Spontaneous resolution enhanced for Stage I (SPON_RES=1)
double k_spon_boost = k_gran_res * (1.0 + SPON_RES * 3.0);

dxdt_GRAN = k_gran_form * gran_drive * (100.0 - gran) * GRAN0
           - k_spon_boost * gran
           - k_gran_res * gran * Edrug_gran * 5.0;

//--------------------------------------------------------------------
// 3h. Pulmonary fibrosis (FIBR)
//     Driven by: chronic granuloma burden (irreversible remodeling)
//     Cannot regress below FIBR0 (established fibrosis)
//     FVC declines proportionally to fibrosis extent
//     Ref: Nardi et al. (2011) Eur Respir J — fibrosis progression in sarc
//--------------------------------------------------------------------
double fibr_drive = gran / 100.0;  // normalized granuloma -> fibrosis
// Fibrosis is quasi-irreversible; no effective drug reversal
// Corticosteroids can slow progression but not reverse established fibrosis
double fibr_floor = FIBR0;
double fibr_incr  = k_fibr * fibr_drive * (k_fibr_max - fibr) * (fibr > fibr_floor ? 1 : 0.1);
// Very slow reversal with effective treatment (clinical observation)
double fibr_decr  = k_fibr * 0.05 * Edrug_gran * fibr;

dxdt_FIBR = fibr_incr - fibr_decr;

//--------------------------------------------------------------------
// 3i. Serum ACE (ACE_BM)
//     Produced by: granuloma macrophages (epithelioid cells)
//     ACE is diagnostic/monitoring biomarker; normal <52 U/L
//     ACE-25 trial: baseline ~45, prednisone -> ~30 at 6 months
//     Ref: Studdy & Bird (1989) — ACE as sarcoidosis activity marker
//--------------------------------------------------------------------
dxdt_ACE_BM = k_ace * gran - k_ace_el * ACE_BM;

//--------------------------------------------------------------------
// 3j. Calcitriol / 1,25(OH)2D (CALIT)
//     Sarcoid macrophages express CYP27B1 -> over-convert 25-OHD to 1,25-D
//     Granuloma burden drives excess calcitriol -> hypercalcemia risk
//     HCQ inhibits CYP27B1 in macrophages (key mechanism)
//     Ref: Adams & Hewison (2012) — HCQ reduces calcitriol in sarcoidosis
//--------------------------------------------------------------------
dxdt_CALIT = k_calit * gran * (1.0 - Ehcq * 0.7) * (1.0 - Epred * 0.4)
            - k_calit_el * CALIT;

//--------------------------------------------------------------------
// 3k. Serum calcium (SERUM_CA)
//     Elevated calcitriol -> increased GI calcium absorption
//     Hypercalcemia (>10.5 mg/dL) in ~10% of sarcoid patients
//     Ref: Sharma (1996) — hypercalcemia in sarcoidosis
//--------------------------------------------------------------------
double calit_excess = (CALIT - CALIT0);  // excess above normal
if (calit_excess < 0) calit_excess = 0;
dxdt_SERUM_CA = k_ca_abs * calit_excess
               - k_ca_el * (SERUM_CA - CA0);  // homeostatic regulation toward CA0

//--------------------------------------------------------------------
// 3l. Serum sIL-2R (SIL2R)
//     Shed from activated T cells (CD4+ Th1 primarily)
//     Strong marker of T cell activation; elevated in active sarcoidosis
//     Ref: Ziegenhagen et al. (2003) — sIL-2R vs. ACE in monitoring
//     Vorselaars trial: sIL-2R decreases with MTX/AZA treatment
//--------------------------------------------------------------------
dxdt_SIL2R = k_sil2r * (th1 + treg * 0.2)  // predominantly shed by Th1
            * (1.0 - Elymph * 0.7)
            - k_sil2r_el * SIL2R;

//--------------------------------------------------------------------
// 3m. FVC % predicted (FVC_P)
//     Declines with fibrosis; improves slightly with inflammation reduction
//     In sarcoidosis: inflammatory component is reversible; fibrotic is not
//     FIRST trial: ~5% improvement with infliximab at 24 weeks (stage II-III)
//     ACE-25: ~3% improvement with prednisone vs. placebo at 6 months
//--------------------------------------------------------------------
// Inflammatory contribution to FVC reduction (reversible)
double inflam_fvc_effect = (gran / GRAN0 - 1.0) * 5.0;  // up to -5% from inflammation
if (inflam_fvc_effect > 0) inflam_fvc_effect = 0;

// Fibrosis contribution (quasi-irreversible)
double fibr_fvc_effect = (fibr - FIBR0) * 30.0;  // each 0.1 unit fibrosis = -3% FVC

// Target FVC: baseline minus fibrotic and inflammatory penalties
double fvc_target = FVC_P0 - fibr_fvc_effect + (gran < GRAN0 ? (GRAN0 - gran) * 0.05 : 0);
double fvc_current = FVC_P;

// First-order approach to target (rate limited)
dxdt_FVC_P = k_fvc * (fvc_target - fvc_current);

$TABLE
//====================================================================
// OUTPUT TABLE — capture PK concentrations + PD biomarkers
//====================================================================

// PK outputs
capture PREDL_conc_ngmL = (PREDL_C / (Vd_predl * WT)) * 1000.0; // ng/mL
capture MTX_plasma_ugL  = MTX_C / (Vd_mtx * WT);                 // mg/L = ug/mL
capture MTXPG_nmolL     = MTX_POLY;                               // nmol/L

// Drug effect indices (fraction inhibition)
capture E_pred_fx  = Emax_pred * pow((PREDL_C / (Vd_predl * WT)) * 1000.0, HILL_n) /
                     (pow(EC50_pred, HILL_n) +
                      pow((PREDL_C / (Vd_predl * WT)) * 1000.0, HILL_n) + 1e-9);
capture E_mtx_fx   = Emax_mtx * pow(MTX_POLY, HILL_n) /
                     (pow(EC50_mtx_poly, HILL_n) + pow(MTX_POLY, HILL_n) + 1e-9);

// PD state outputs
capture MACROPHAGE  = MAC_ACT;
capture TH1_CELLS   = TH1;
capture TREG_CELLS  = TREG;
capture TNF_pg_mL   = TNF;
capture IFNG_pg_mL  = IFNG;
capture IL12_pg_mL  = IL12;
capture GRANULOMA   = GRAN;
capture FIBROSIS    = FIBR;
capture ACE         = ACE_BM;
capture CALCITRIOL  = CALIT;
capture CALCIUM     = SERUM_CA;
capture SIL2R_UmL   = SIL2R;
capture FVC_pct     = FVC_P;

// Derived clinical outcome flags
capture HYPERCALCEMIA = (SERUM_CA > 10.5) ? 1.0 : 0.0;
capture ACE_ELEVATED  = (ACE_BM   > 52.0) ? 1.0 : 0.0;
capture FVC_IMPAIRED  = (FVC_P    < 70.0) ? 1.0 : 0.0;
'

